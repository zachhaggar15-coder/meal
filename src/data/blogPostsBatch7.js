function sourcesFor(cluster, extra = []) {
  const keys = ['eatwell', 'fsaChill', 'fsaCooking'];
  if (cluster === 'weight') keys.splice(1, 0, 'healthyWeight');
  return [...new Set([...keys, ...extra])].map(key => SOURCES[key]);
}

function contextualLinksFor(topic) {
  const blocks = [
    ...planFinderLinks,
    ...(clusterLinks[topic.cluster] || []),
  ];

  if (topic.supermarketSlug) {
    blocks.push({
      parts: [
        { text: `For ${topic.supermarketName} plans specifically, open the ` },
        { label: `${topic.supermarketName} meal plan page`, to: `/choose-supermarket/${topic.supermarketSlug}` },
        { text: ' and choose the calorie target or diet type that fits your week.' },
      ],
    });
  }

  return blocks;
}

function relatedFor(topic) {
  const explicit = topic.related || [];
  const defaults = defaultRelated[topic.cluster] || defaultRelated.mealPrep;
  const combined = [...explicit, ...defaults, { path: '/quiz', label: 'Find My Meal Plan', type: 'guide' }];
  const seen = new Set();

  return combined
    .filter(item => item.slug !== topic.slug)
    .filter(item => {
      const key = item.path || item.slug;
      if (seen.has(key)) return false;
      seen.add(key);
      return true;
    })
    .slice(0, 6);
}

function shoppingBullets(shopping = {}) {
  return Object.entries(shopping).map(([label, items]) => (
    `${label}: ${items.join(', ')}.`
  ));
}

